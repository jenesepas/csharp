class CMostrarDatos
{
  /*
   * Mostrar datos 
   */

  public static void Main(string[] args)
  {
    double a = 0, b = 0;
    int c = 0;
    string s;

    a = 3.14; b = 2.71; c = 2;
    s = "Datos";

    System.Console.WriteLine(s + ": ");
    System.Console.WriteLine(" a = " +  a);
    System.Console.WriteLine(" b = " +  b);
    System.Console.WriteLine(" c = " +  c);
  }
}
